using namespace std;

const int N = 1e5;
vector<pair<int,int> > adj[N] = {};

void path(int n,int u)
{
  priority_queue<pair<int,int> , vector<pair<int,int> > , greater<int> > pq;
  vector<int> dist(n,INF);

  pq.push(mp(0,u));
  dist[u] = 0;
  while(!pq.empty())
  {
    int u = pq.top().se;
    pq.pop();

    for(auto p : adj[u])
    {
      int v = p.se, weight = p.fi;
      if(dist[v]> dist[u]+weight)
      {
        dist[v] = dist[u] + weight;
        pq.push(mp(dist[v],v));
      }
    }
  }
}

