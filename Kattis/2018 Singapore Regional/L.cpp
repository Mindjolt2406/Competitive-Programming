void sieve(ll upperbound)
{
  ll size = upperbound + 1;
  bs.set();
  bs[0] = bs[1] = false;
  for(ll i = 2;i< size;i++)
  {
    if(bs[i])
    {
      counter[i][i]++;
      for(ll j = 2*i;j<size;j+=i) 
      {
        int k = j;
        while(k%i==0) {counter[j][i]++; k/=i;}
        bs[j] = false;
      }
    }
  }
}

