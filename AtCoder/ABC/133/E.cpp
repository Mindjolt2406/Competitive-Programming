const int N = 1e5+10;
int k;
vector<int> adj[N] = {};

int dfs(int u = 0,int p = -1, int depth = 0,int val = 1)
{
  bool isLeaf = true;
  if(depth == 0) {val*=k; val %= MOD;}
  else if(depth==1) {val*=(k-1); val %= MOD;}
  else if(depth>=2) {val*=(k-2); val %= MOD;}

  int cnt = 0;
  for(auto v : adj[u])
  {
    if(v-p)
    {
      isLeaf = false;
      cnt += dfs(v,u,depth+1,val);
      if(cnt>=MOD) cnt -= MOD;
    }
  }

  if(isLeaf) cnt = val;

  return cnt;
  
}

