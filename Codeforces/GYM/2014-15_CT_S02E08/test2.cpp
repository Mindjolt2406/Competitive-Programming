const int N = 1e5+10;

vector<int> adj[N] = {};
vector<int> col(N,-1);
vector<int> vis(N);

void dfs(int u = 0, int colour_of_parent = 0)
{
  vis[u] = 1;
  col[u] = colour_of_parent ^ 1;
  for(auto child : adj[u])
  {
    if(!vis[child])
    {
      dfs(child,col[u]);
    }
  }
}

